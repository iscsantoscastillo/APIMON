IF EXISTS 
        (SELECT 
             TABLE_NAME 
         FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME = 'CAT_LOCALIDADES') 
DROP TABLE CAT_LOCALIDADES;
GO
CREATE TABLE CAT_LOCALIDADES(
    ENTIDAD_ID INT NOT NULL,         			-- Identificador de la Entidad. Tabla CAT_ENTIDADES
	MUNICIPIO_ID INT NOT NULL,					-- Identificador de la Entidad. Tabla CAT_MUNICIPIOS
	LOCALIDAD_ID INT NOT NULL,					-- Identificador de la Entidad. Tabla CAT_LOCALIDADES
    NOMBRE VARCHAR(200) NOT NULL,				-- Nombre de la Localidad
	PRIMARY KEY (ENTIDAD_ID, MUNICIPIO_ID, LOCALIDAD_ID)
);
GO